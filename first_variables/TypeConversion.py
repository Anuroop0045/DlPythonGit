# Type conversions means 'CONVERTING FOROM ONE DATA TYPE TO ANOTHER DATA TYPE'

a = 20
b = 2.5
print(a+b)

a1 = 100
a2 = float(a1)
print(a1)
print(type(a2))
print(a2)   # output will be 100.0 (converting 100 to float i.e 100.0)

b1 = 22.5
b2 = int(b1)
print(b1)
print(b2)
print(type(b2))

c1 = 20.5
c2 = int(c1)+5
print(c1)
print(c2)

d1 = 10
d2 = str(d1)
print(d1)
print(d2)
print(type(d2))